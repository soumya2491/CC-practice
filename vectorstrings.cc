#include <iostream>
#include <vector>
int main(void){
    std::vector<char> name;
    char ch;
    while(std::cin.get(ch) && ch!='\n'){
        name.push_back(ch);
    }
    for(char ch: name){
        std::cout << ch << '\n';
    }
    return 0;
}



















